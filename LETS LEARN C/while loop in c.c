///printing even numbers from 1-50 using while loop
#include <stdio.h>
int main()
{
    int i = 2;                                  ///            syntax:

    while(i<=50){                              ///              while(condition){

       printf("%d\n",i);                      ///               code to be executed ;

        i=i+2;                               ///                increment/decrement ;
    }
    return 0;                               ///                }
}
