//write a program in the programming language c to use and compare two different loops while and for
#include<stdio.h> //preprocessor command for input output header file
int main()       //main function
{                   //beginning of main function
   int n=2;        //declaring and initializing variable
   while(n<=10) {     //condition and beginning of the while loop
    printf(" %d\n",n);     //printing the value of n after each iteration
    n=n+2;               //n increment by 2
   }                        //ending of while loop
   return 0;//indicate that program ended

 }    //ending main function
